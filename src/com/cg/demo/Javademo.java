package com.cg.demo;

public class Javademo {
	public static void main(String[] args) {
		int num = 10;
		System.out.println(num);
		num++;
		System.out.println(num);
		System.out.println(num++);
		System.out.println(num);
		System.out.println(++num);
		System.out.println(num);
		System.out.println(--num);
		System.out.println(num--);
		System.out.println(num);
	}

}
